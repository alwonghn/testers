package com.smva.jb;

/**
 *
 * @author alfred.wong
 */
public class RoleBean {

    private int role_ind;
    private String role_name;
    private String role_desc;
    private boolean supervisor_role;

    public RoleBean() {
        role_ind = 0;
        supervisor_role = false;
    }

    public RoleBean(int role_ind, String role_name, String role_desc, boolean supervisor_role){
        this.role_ind = role_ind;
        this.role_name = role_name;
        this.role_desc = role_desc;
        this.supervisor_role = supervisor_role;
    }

    public String getRole_desc() {
        return role_desc;
    }

    public void setRole_desc(String role_desc) {
        this.role_desc = role_desc;
    }

    public int getRole_ind() {
        return role_ind;
    }

    public void setRole_ind(int role_ind) {
        this.role_ind = role_ind;
    }

    public String getRole_name() {
        return role_name;
    }

    public void setRole_name(String role_name) {
        this.role_name = role_name;
    }

    public boolean isSupervisor_role() {
        return supervisor_role;
    }

    public void setSupervisor_role(boolean supervisor_role) {
        this.supervisor_role = supervisor_role;
    }




}
